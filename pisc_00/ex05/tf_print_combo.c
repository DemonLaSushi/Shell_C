#include <unistd.h>
#include <stdio.h>

void	ft_print_combo(void)
{
	int	c;
	int	u;
	int	m;

	c = '0';
	while (c <= '7')
	{
	u = c + 1;
		while (u <= '8')
		{
				m = u + 1;
			while (m <= '9')
			{
				write(1, &c, 1);
				write(1, &u, 1);
				write(1, &m, 1);
				if (c < '7')
					write(1, ", ", 2);
				m++;
			}
			u++;
		}
		c++;
	}
}
